// 結果から発動中のセットをカウントするユーティリティ関数
export function getActiveSets(pieces: Record<string, any>): Record<string, number> {
  const counts: Record<string, number> = {};
  Object.values(pieces).forEach(p => {
    if (p && p.setName && p.setName !== "その他" && p.setName !== "未選択") {
      counts[p.setName] = (counts[p.setName] || 0) + 1;
    }
  });
  return counts;
}
